<p>After you add the event tracking code to the Contact Form 7 plugin, you may want to setup a goal in Google Analytics that's related to the event. Follow these instructions to setup the goal.</p>
<h3>Redirection Goal:</h3>
<ul>
	<li>1.	Login to Google Analytics and click on Admin in the main navigation.</li>
	<li>2.	Select the Account and Property where you want to create the goal. Under the View list, click on Goals</li>
	<li>
		3.	Click on the New Goal button, click on the Custom button and then click on the Next step button.
		<img src="<?php echo CF7_SUBMIT_PLUGIN_URL.'admin/image/1.jpg'?>">
	
	</li>
	
	<li>4.	Name the goal and select the Destination radio button.
		<img src="<?php echo CF7_SUBMIT_PLUGIN_URL.'admin/image/2.jpg'?>">
	</li>
	<li>5.	Enter the URL of your thank you page.
		<img src="<?php echo CF7_SUBMIT_PLUGIN_URL.'admin/image/3.jpg'?>">
	</li>
	<li>6.	Click the Create Goal button. You're done!</li>
</ul>

<h3>On-Submit Goal:</h3>

<ul>
	<li>1.	Login to Google Analytics and click on Admin in the main navigation.</li>
	<li>2.	Select the Account and Property where you want to create the goal. Under the View list, click on Goals</li>
	<li>3.	Click on the New Goal button, click on the Custom button and then click on the Next step button.
		<img src="<?php echo CF7_SUBMIT_PLUGIN_URL.'admin/image/4.jpg'?>">
	</li>
	<li>4.	Name the goal and select the Destination radio button.
		<img src="<?php echo CF7_SUBMIT_PLUGIN_URL.'admin/image/5.jpg'?>">
	</li>
	<li>
		5.	Populate all of the relevant goal details that will match the values you have input in the Contact Form 7 plugin:
	<ul class="sub">
		<li>•	Category | Equals to</li>
		<li>•	Action | Equals to</li>
		<li>•	Label | Equals to</li>
		<li>•	Value | Equals to</li>
	</ul>
		<img src="<?php echo CF7_SUBMIT_PLUGIN_URL.'admin/image/6.jpg'?>">
	</li>
</ul>

